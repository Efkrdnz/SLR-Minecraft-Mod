	@Override
	protected void registerGoals() {
		super.registerGoals();
		this.goalSelector.addGoal(1, new OwnerHurtByTargetGoal(this));
		this.targetSelector.addGoal(2, new OwnerHurtTargetGoal(this));
		this.goalSelector.addGoal(3, new AvoidEntityGoal<>(this, FireFlyEntity.class, (float) 6, 1, 1.2));
		this.targetSelector.addGoal(4, new NearestAttackableTargetGoal(this, LivingEntity.class, false, false));
		this.goalSelector.addGoal(5, new Goal() {
			{
				this.setFlags(EnumSet.of(Goal.Flag.MOVE));
			}

			public boolean canUse() {
				if (FireFlyEntity.this.getTarget() != null && !FireFlyEntity.this.getMoveControl().hasWanted()) {
					return true;
				} else {
					return false;
				}
			}

			@Override
			public boolean canContinueToUse() {
				return FireFlyEntity.this.getMoveControl().hasWanted() && FireFlyEntity.this.getTarget() != null && FireFlyEntity.this.getTarget().isAlive();
			}

			@Override
			public void start() {
				LivingEntity livingentity = FireFlyEntity.this.getTarget();
				Vec3 vec3d = livingentity.getEyePosition(1);
				FireFlyEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 3);
			}

			@Override
			public void tick() {
				LivingEntity livingentity = FireFlyEntity.this.getTarget();
				if (FireFlyEntity.this.getBoundingBox().intersects(livingentity.getBoundingBox())) {
					FireFlyEntity.this.doHurtTarget(livingentity);
				} else {
					double d0 = FireFlyEntity.this.distanceToSqr(livingentity);
					if (d0 < 16) {
						Vec3 vec3d = livingentity.getEyePosition(1);
						FireFlyEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 3);
					}
				}
			}
		});
		this.goalSelector.addGoal(6, new RandomStrollGoal(this, 3, 20) {
			@Override
			protected Vec3 getPosition() {
				RandomSource random = FireFlyEntity.this.getRandom();
				double dir_x = FireFlyEntity.this.getX() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_y = FireFlyEntity.this.getY() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_z = FireFlyEntity.this.getZ() + ((random.nextFloat() * 2 - 1) * 16);
				return new Vec3(dir_x, dir_y, dir_z);
			}
		});
		this.goalSelector.addGoal(7, new RandomLookAroundGoal(this));
		this.goalSelector.addGoal(8, new LeapAtTargetGoal(this, (float) 0.5));
	}

