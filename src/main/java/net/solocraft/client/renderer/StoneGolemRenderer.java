
package net.solocraft.client.renderer;

import software.bernie.geckolib.renderer.GeoEntityRenderer;
import software.bernie.geckolib.cache.object.BakedGeoModel;

import net.solocraft.entity.model.StoneGolemModel;
import net.solocraft.entity.layer.StoneGolemLayer;
import net.solocraft.entity.StoneGolemEntity;

import net.minecraft.resources.ResourceLocation;
import net.minecraft.client.renderer.entity.EntityRendererProvider;
import net.minecraft.client.renderer.RenderType;
import net.minecraft.client.renderer.MultiBufferSource;

import com.mojang.blaze3d.vertex.VertexConsumer;
import com.mojang.blaze3d.vertex.PoseStack;

public class StoneGolemRenderer extends GeoEntityRenderer<StoneGolemEntity> {
	public StoneGolemRenderer(EntityRendererProvider.Context renderManager) {
		super(renderManager, new StoneGolemModel());
		this.shadowRadius = 0.5f;
		this.addRenderLayer(new StoneGolemLayer(this));
	}

	@Override
	public RenderType getRenderType(StoneGolemEntity animatable, ResourceLocation texture, MultiBufferSource bufferSource, float partialTick) {
		return RenderType.entityTranslucent(getTextureLocation(animatable));
	}

	@Override
	public void preRender(PoseStack poseStack, StoneGolemEntity entity, BakedGeoModel model, MultiBufferSource bufferSource, VertexConsumer buffer, boolean isReRender, float partialTick, int packedLight, int packedOverlay, float red, float green,
			float blue, float alpha) {
		float scale = 4f;
		this.scaleHeight = scale;
		this.scaleWidth = scale;
		super.preRender(poseStack, entity, model, bufferSource, buffer, isReRender, partialTick, packedLight, packedOverlay, red, green, blue, alpha);
	}

	@Override
	protected float getDeathMaxRotation(StoneGolemEntity entityLivingBaseIn) {
		return 0.0F;
	}
}
